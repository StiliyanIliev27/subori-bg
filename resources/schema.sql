-- ============================================================
--  Събори и Местни Събития в България
--  Database Schema — 3NF
-- ============================================================

CREATE DATABASE IF NOT EXISTS sabori_bg
    CHARACTER SET utf8mb4
    COLLATE utf8mb4_unicode_ci;

USE sabori_bg;

-- ============================================================
--  TABLE: regions
-- ============================================================
CREATE TABLE regions (
    id   INT          NOT NULL AUTO_INCREMENT,
    name VARCHAR(100) NOT NULL,
    PRIMARY KEY (id),
    CONSTRAINT uq_regions_name UNIQUE (name)
);

-- ============================================================
--  TABLE: settlements
-- ============================================================
CREATE TABLE settlements (
    id        INT          NOT NULL AUTO_INCREMENT,
    name      VARCHAR(100) NOT NULL,
    type      ENUM('село','град') NOT NULL,
    region_id INT          NOT NULL,
    PRIMARY KEY (id),
    CONSTRAINT fk_settlements_region
        FOREIGN KEY (region_id) REFERENCES regions(id)
        ON UPDATE CASCADE ON DELETE RESTRICT
);

-- ============================================================
--  TABLE: categories
-- ============================================================
CREATE TABLE categories (
    id   INT          NOT NULL AUTO_INCREMENT,
    name VARCHAR(100) NOT NULL,
    PRIMARY KEY (id),
    CONSTRAINT uq_categories_name UNIQUE (name)
);

-- ============================================================
--  TABLE: organizers
-- ============================================================
CREATE TABLE organizers (
    id            INT          NOT NULL AUTO_INCREMENT,
    name          VARCHAR(200) NOT NULL,
    type          ENUM('Община','Читалище','Частен') NOT NULL,
    contact       VARCHAR(200)     NULL,
    settlement_id INT          NOT NULL,
    PRIMARY KEY (id),
    CONSTRAINT fk_organizers_settlement
        FOREIGN KEY (settlement_id) REFERENCES settlements(id)
        ON UPDATE CASCADE ON DELETE RESTRICT
);

-- ============================================================
--  TABLE: events
-- ============================================================
CREATE TABLE events (
    id            INT          NOT NULL AUTO_INCREMENT,
    name          VARCHAR(200) NOT NULL,
    event_date    DATE         NOT NULL,
    is_recurring  BOOLEAN      NOT NULL DEFAULT TRUE,
    description   TEXT             NULL,
    settlement_id INT          NOT NULL,
    category_id   INT          NOT NULL,
    organizer_id  INT          NOT NULL,
    PRIMARY KEY (id),
    CONSTRAINT fk_events_settlement
        FOREIGN KEY (settlement_id) REFERENCES settlements(id)
        ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_events_category
        FOREIGN KEY (category_id) REFERENCES categories(id)
        ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_events_organizer
        FOREIGN KEY (organizer_id) REFERENCES organizers(id)
        ON UPDATE CASCADE ON DELETE RESTRICT
);

